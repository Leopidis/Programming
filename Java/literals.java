public class literals {
    public static void main(String[] args) {
        double d = 3.14;
        System.out.println(d);
        long l = 149L;
        System.out.println(l);
        boolean b = false;
        System.out.println(b);
        double d2 = 1E0;
        System.out.println(d2);
        int i = 899;
        System.out.println(i);
        int i2 = 0xA0;
        System.out.println(i2);
        char c = '\u0041';
        System.out.println(c);
    }
}
